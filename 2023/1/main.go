func part1(input string) {
	scan := bufio.NewScanner(strings.NewReader(input))

	var total int
	for scan.Scan() {
		line := scan.Bytes()

		var first, last int
		first = -1
		for _, c := range line {
			if c >= '0' && c <= '9' {
				if first == -1 {
					first = int(c - '0')
				}
				last = int(c - '0')
			}
		}

		total += first*10 + last

	}
	fmt.Println(total)
}

var numbers = []string{"one", "two", "three", "four", "five", "six", "seven", "eight", "nine"}

func part2(input string) {
	scan := bufio.NewScanner(strings.NewReader(input))

	var total int
	var count int
	for scan.Scan() {
		line := scan.Bytes()

		var first, last int
		first = -1
		for i := 0; i < len(line); i++ {
			c := line[i]
			if c >= '0' && c <= '9' {
				if first == -1 {
					first = int(c - '0')
				}
				last = int(c - '0')
			} else {
				for j, n := range numbers {
					if i+len(n) <= len(line) && string(line[i:i+len(n)]) == n {
						if first == -1 {
							first = j + 1
						}
						last = j + 1
						// Can skip ahead, but some numbers start with letters
						// at the end of the previous number and loop will add
						// another 1 to i
						i += len(n) - 2
						break
					}
				}
			}
		}

		total += first*10 + last
		count++
		fmt.Printf("line %d, %d %d total: %d %s\n", count, first, last, total, line)

	}
	fmt.Println(total)
}

