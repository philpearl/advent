func part2(input string) {
	var count int
	for i, c := range input {
		if c != '.' {
			continue
		}
		// Make copy of input with current position replaced with '#'
		new := input[:i] + "#" + input[i+1:]

		a := newArena(new)

		if hasLoop(a) {
			count += 1
		}
	}

	fmt.Println(count)
}

func hasLoop(a *arena) bool {
	type state struct {
		pos  point
		dirn point
	}

	current := findGuard(a)
	dirn := point{0, -1}
	visited := make(map[state]struct{})

	for {
		visited[state{pos: current, dirn: dirn}] = struct{}{}

		next := point{current.x + dirn.x, current.y + dirn.y}

		if !a.valid(next) {
			return false
		}

		if c := a.get(next); c == '#' {
			// Rotate right
			dirn = point{-dirn.y, dirn.x}
		} else {
			current = next
		}

		if _, ok := visited[state{pos: current, dirn: dirn}]; ok {
			return true
		}
	}
}

func findGuard(a *arena) point {
	for p := range a.points() {
		if a.get(p) == '^' {
			return p
		}
	}
	panic("guard not found")
}

type arena struct {
	grid []string
}

func newArena(input string) *arena {
	a := &arena{}
	a.grid = strings.Split(input, "\n")
	return a
}

type point struct{ x, y int }

func (a *arena) get(p point) byte {
	return a.grid[p.y][p.x]
}

func (a *arena) valid(p point) bool {
	return p.x >= 0 && p.x < len(a.grid[0]) && p.y >= 0 && p.y < len(a.grid)
}

func (a *arena) points() iter.Seq[point] {
	return func(yield func(point) bool) {
		for y, row := range a.grid {
			for x := range row {
				if !yield(point{x, y}) {
					return
				}
			}
		}
	}
}

