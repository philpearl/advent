func part1(input string) {
	lines := strings.Split(input, "\n")

	type slope struct {
		x int
		y int
	}

	slopes := []slope{
		{x: 1, y: 1},
		{x: 3, y: 1},
		{x: 5, y: 1},
		{x: 7, y: 1},
		{x: 1, y: 2},
	}

	w := len(lines[0])
	var m int = 1
	for _, s := range slopes {
		var x, y, trees int

		for y < len(lines) {
			if lines[y][x] == '#' {
				trees++
			}
			y += s.y
			x = (x + s.x) % w
		}
		fmt.Println(s, trees)
		m *= trees
	}
	fmt.Println(m)
}

