package main

import (
	"container/list"
	"fmt"
	"strings"
)

func main() {
	fmt.Print("\033[2J")
	fmt.Print("\033[0;0f")

	a := newArena(input)
	// a.print()

	p := problem{
		a:          a,
		visited:    make(map[state]struct{}, a.width*a.height*10),
		possibles:  list.New(),
		goalPoints: a.findGoal(),
	}

	start := a.find('0')
	p.add(possible{
		state: state{
			coord:  start,
			points: 1 << 0,
		},
	})

	for {
		poss, ok := p.next()
		if !ok {
			fmt.Printf("\033[%d;%dfno more possibilities\n", a.height+1, 0)
			break
		}

		// poss.coord.print(byte('o'))

		if poss.points == p.goalPoints && poss.coord == start {
			fmt.Printf("\033[%d;%dfMade it at depth %d\n", a.height+1, 0, poss.depth)
			break
		}

		for _, move := range moves {
			if p.isMovePossible(poss.state, move) {
				p.add(possible{
					state: p.changeState(poss.state, move),
					depth: poss.depth + 1,
				})
			}
		}
	}

}

type problem struct {
	a          *arena
	visited    map[state]struct{}
	possibles  *list.List
	goalPoints int
}

func (p *problem) isMovePossible(s state, dirn coord) bool {
	to := s.coord.add(dirn)
	what := p.a.at(to)
	return what != '#'
}

func (p *problem) changeState(s state, dirn coord) state {
	to := s.coord.add(dirn)
	what := p.a.at(to)

	if what != '.' {
		point := what - '0'
		s.points |= 1 << point
	}

	s.coord = to

	return s
}

func (p *problem) add(s possible) {
	if _, ok := p.visited[s.state]; ok {
		return
	}
	p.visited[s.state] = struct{}{}

	p.possibles.PushBack(s)
}

func (p *problem) next() (possible, bool) {
	e := p.possibles.Front()
	if e == nil {
		return possible{}, false
	}
	p.possibles.Remove(e)
	return e.Value.(possible), true
}

type state struct {
	// We track whether we've been at this point before with points visited
	coord
	// We track points we've visited by setting bits
	points int
}

type possible struct {
	state
	depth int
}

type coord struct{ x, y int }

func (c1 coord) add(c2 coord) coord {
	return coord{
		x: c1.x + c2.x,
		y: c1.y + c2.y,
	}
}

func (c coord) print(b byte) {
	fmt.Printf("\033[%d;%df%c", c.y+1, c.x+1, b)
}

var (
	DOWN  = coord{0, 1}
	UP    = coord{0, -1}
	LEFT  = coord{-1, 0}
	RIGHT = coord{1, 0}
)

var moves = []coord{UP, DOWN, LEFT, RIGHT}

type arena struct {
	data          string
	width, height int
}

func newArena(input string) *arena {
	ll := len(input)
	width := strings.IndexByte(input, '\n')
	height := ll/(width+1) + 1

	return &arena{
		data:   input,
		width:  width,
		height: height,
	}
}

func (a *arena) findGoal() int {
	var goal int
	for _, c := range a.data {
		if c >= '0' && c <= '9' {
			goal |= 1 << uint(c-'0')
		}
	}
	return goal
}

func (a *arena) at(c coord) byte {
	if c.x < 0 || c.x >= a.width || c.y < 0 || c.y >= a.height {
		return '#'
	}
	return a.data[c.y*(a.width+1)+c.x]
}

func (a *arena) find(c byte) coord {
	offset := strings.IndexByte(a.data, c)
	y := offset / (a.width + 1)
	return coord{
		x: offset - (y * (a.width + 1)),
		y: y,
	}
}

func (a *arena) print() {
	for y := 0; y < a.height; y++ {
		for x := 0; x < a.width; x++ {
			fmt.Printf("%c", a.at(coord{x, y}))
		}
		fmt.Print("\n")
	}
}

